namespace Learn.M03.Examples;

public static class ArithmeticExample
{
  public static void Run()
  {
    int a = 17;
    int b = 5;
    Console.WriteLine($"{a} + {b} = {a + b}");
    Console.WriteLine($"{a} - {b} = {a - b}");
    Console.WriteLine($"{a} * {b} = {a * b}");
    Console.WriteLine($"{a} / {b} = {a / b}");
    Console.WriteLine($"{a} % {b} = {a % b}");
  }
}
